        String query = "";
        if(s.equals("*")){
            query = "select " + s + " from file_table";
        }
        else{
            query = "select * from file_table where fileName = '" + s + "'";
        }
        
        ArrayList<String> values = c.sendQueryToServer(query, 2);
        if(values.isEmpty()){
            
        }else{
            fillTable(values);
        }
        
