    public void fillTable(ArrayList<String> values){
        DefaultTableModel dft = (DefaultTableModel) filesTable.getModel();
        dft.setRowCount(0);
        int i = 0,j = 0;
        
        for(String str : values){
            String s = (i+1) +"#" + str;
            String[] tokens = s.split("#");
            
            dft.addRow(tokens);
            i++;
        }
    }
